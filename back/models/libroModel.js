'use strict';


//////////////////////////////////////////////////////////////////////
///////////***********     llamo a moongoose        ****//////////////
//////////////////////////////////////////////////////////////////////
let mongoose = require('mongoose');
let Schema=mongoose.Schema;


//////////////////////////////////////////////////////////////////////////////
////////***********     creo el esquema / productos        ****///////////////
//////////////////////////////////////////////////////////////////////////////
let Libro = new Schema({
	titulo: {type: Schema.Types.ObjectId, ref:"Titulo"},
	autor:String,
	isbn:String,
	descripcion:String,
	estado:String,
	cVenta:Boolean,
	cIntercambio:Boolean,
	cCompartir:Boolean,
	activo:Boolean,
	precio:String,
	genero:{type: Schema.Types.ObjectId, ref:"Categoria"},
	categoria: {type: Schema.ObjectId, ref:'Categoria'},
	usuario: {type: Schema.ObjectId, ref:'User'},
})

//////////////////////////////////////////////////////////////////////////////
////////***********    exporto el esquema        ****/////////////////////////
//////////////////////////////////////////////////////////////////////////////
module.exports = mongoose.model('Libro', Libro)
