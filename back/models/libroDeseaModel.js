'use strict';


//////////////////////////////////////////////////////////////////////
///////////***********     llamo a moongoose        ****//////////////
//////////////////////////////////////////////////////////////////////
let mongoose = require('mongoose');
let Schema=mongoose.Schema;
let moment   = require('moment');
//////////////////////////////////////////////////////////////////////////////
////////***********     creo el esquema / productos        ****///////////////
//////////////////////////////////////////////////////////////////////////////
let LibroDesea = new Schema({
	activo:String,
	titulo: {type: Schema.Types.ObjectId, ref:"Titulo"},
	usuario: {type: Schema.ObjectId, ref:'User'},
	createdAt: {type: String, default: moment().format('YYYY-MM-DD h:mm:ss') },
})

//////////////////////////////////////////////////////////////////////////////
////////***********    exporto el esquema        ****/////////////////////////
//////////////////////////////////////////////////////////////////////////////
module.exports = mongoose.model('LibroDesea', LibroDesea)
