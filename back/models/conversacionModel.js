let mongoose = require('mongoose')
let Schema   = mongoose.Schema;
let moment   = require('moment');


 

//////////////////////////////////////////////////////////////////////////////
////////***********     creo el esquema / ciudad        ****//////////////
//////////////////////////////////////////////////////////////////////////////
let Conversacion = new Schema({
	libroId:  {type: Schema.ObjectId, ref:'Libro'},
	tituloId:  {type: Schema.ObjectId, ref:'Titulo'},
	usuario1: {type: Schema.ObjectId, ref:'User'},
	usuario2: {type: Schema.ObjectId, ref:'User'},
	tipo:     String,
	estado: Number,
	updatedAt:  { type: String, default: moment().format('YYYY-MM-DD h:mm:ss') },
})


//////////////////////////////////////////////////////////////////////////////
////////***********    exporto el esquema        ****/////////////////////////
//////////////////////////////////////////////////////////////////////////////


module.exports = mongoose.model('Conversacion', Conversacion)