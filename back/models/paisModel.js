let mongoose = require('mongoose')
let Schema   = mongoose.Schema;



//////////////////////////////////////////////////////////////////////////////
////////***********     creo el esquema / pais        ****//////////////
//////////////////////////////////////////////////////////////////////////////
let Pais = new Schema({
	country: String,
	abbreviation: String,
})


 


//////////////////////////////////////////////////////////////////////////////
////////***********    exporto el esquema        ****/////////////////////////
//////////////////////////////////////////////////////////////////////////////

module.exports = mongoose.model('Pais', Pais)
 